<h1 > Tesla Model S wins 'Car of the year' </h1>

<span class = "firstpicture" >
<img src="Images/ModelSphoto4.jpg"
width = "700"
height = "400"/>
</span>


<p>Today Model S was named Motor Trend’s 2013 Car of the Year, one of the automotive industry’s most coveted awards. To take top honors, Model S 
garnered a unanimous vote from the panel of Motor Trend judges and guest judges, considered among the savviest and toughest critics in the industry. 
This is the first time in Motor Trend’s memory that every judge was in unanimous agreement.
</br></br>
“Our aspiration with the Model S was to show that an electric car truly can be better than any gasoline car, which is a critical step towards 
the widespread adoption of sustainable transport,” said Elon Musk, Tesla Motors co-founder and CEO. “Nothing illustrates this more clearly than
 winning Motor Trend’s Car of the Year by unanimous decision against a field of exceptional competitors.”
</br> </br>
The 2013 field of competition included entries from some of the most historically dominant nameplates. The field was initially narrowed to 
11 finalists including the Porsche Boxster, BMW 3-series, Lexus GS, and Subaru BRZ. After extensive track and road testing pushed vehicles
 to their limits in one of the industry’s toughest tests of performance, efficiency, safety and more, the Tesla Model S was awarded top honors.
 </br>
</p>

