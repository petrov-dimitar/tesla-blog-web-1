<b> About Model S</b>
</br>
</br>
Model S is the world’s first premium sedan built from the ground up as an electric vehicle and has been engineered to elevate the 
public’s expectations of what a premium sedan can be. At the heart of Model S is the proven Tesla powertrain, delivering both
 unprecedented range and a thrilling drive experience. With a rigid body structure, nearly 50/50 weight distribution and a remarkably 
 low center of gravity, Model S offers the responsiveness and agility expected from the world’s best sports cars while providing the ride 
 quality of a luxury performance sedan.
</br></br>
 

Setting the bar for electric driving range, Model S offers 40 kWh, 60 kWh and 85 kWh battery options. All deliver unprecedented range,
 with the 85 kWh variant achieving an EPA-certified range of 265 miles. All three batteries use automotive-grade lithium-ion cells arranged
 for optimal energy density, thermal management, and safety.
</br></br>
Without an internal combustion engine or transmission tunnel, the interior of Model S has more cargo space than any other sedan in its
 class and includes a second trunk under the hood. The Model S interior features a 17” in-dash touchscreen with Internet capabilities, 
 allowing for streaming radio, web browsing and navigation.
 </br>
 </br>
 <b>About Tesla</b>
 </br></br>
Tesla’s goal is to accelerate the world’s transition to electric mobility with a full range of increasingly affordable electric cars. 
California-based Tesla designs and manufactures EVs, as well as EV powertrain components for partners such as Toyota and Daimler. 
Tesla has delivered more than 2,400 Roadsters to customers worldwide. Model S, the world’s first premium sedan to be engineered 
from the ground up as an electric vehicle, began deliveries to U.S. customers in June of this year. Tesla has already received more than 13,000 
reservations worldwide for Model S.
