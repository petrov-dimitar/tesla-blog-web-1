<h1 > Tesla Roadster is officially the first car in space </h1>

<span class = "firstpicture" >
<img src="Images/TeslainspaceArticle.jpg"
width = "700"
height = "400"/>
</span>


<p> Ladies and gentlemen, Elon Musk has sent his cherry red Tesla Roadster into space. 

There is a car currently doing an orbit in space. A. Car. In. Space.
Just let that sink in.
</br></br>
Last night, Musk’s SpaceX completed a rather successful launch of the Falcon Heavy rocket - now the most powerful rocket on earth - from the 
Kennedy Space Center in Florida. The low-cost rocket (comparatively) 
is apparently capable of delivering a payload of 64 tonnes. On this test launch however, there was a slightly smaller load.

Musk’s Roadster. It was strapped onto the rocket - as promised - complete with a dummy in a space suit, arm nonchalantly r
esting on the side door, with David Bowie’s music set on a loop (just think about it). It blasted off into space successfully,
 while the the boost rockets returned to earth in controlled landings (again, just think about it).
</p>



<span class = "midpicture" >
<img src="Images/Teslainspace(mid).jpg"
width = "700"
height = "400"/>
</span>



<p> 
The Falcon Heavy launched from Cape Canaveral in Florida at about 3:45 pm ET on Tuesday in a history-making event.
 Two of the rocket’s three boosters detached and returned to Earth, touching down on landing sites nearby. The last booster wasn’t as lucky. </br></br>
 Musk told reporters at a post-launch press conference Tuesday night that the booster missed its mark on a SpaceX drone ship in the Atlantic Ocean
 and hit the water going at 300 miles per hour.

The upper stage of the rocket—the bit with the Tesla in tow—made it into space. There, after a six-hour coast, the stage will restart its engine
 and push the Tesla into an elliptical orbit between Earth and Mars. The car will join the planets and comets and everything else in their steady march
 around the sun. Over time, the car’s orbit is expected to bring it closer to Mars, which is what Musk means when he says he’s sending his car to 
 the planet.

Musk told reporters in a teleconference Monday he expects the Tesla to coast comfortably in space for hundreds of millions of years.</br>
 “At times it will come extremely close to Mars,” he said. “And there’s a tiny chance that it will hit Mars. Extremely tiny.”</br></br>

The Tesla is, well, not the Golden Record. Instead of a rosy time capsule of Earth and its history and inhabitants made for consumption by unknowable
 alien civilizations, it carries a dummy in a SpaceX space suit, and will blast David Bowie’s “Space Oddity” at top volume from its speakers. 
 Any other payload, Musk said when he made this public in December, would have been “boring.”</br></br>

The decision to make the payload a Tesla was a clever show of cross-promotional marketing that only someone like Musk is capable of.
 Some have groaned at the idea, describing it as nothing more than a cheesy publicity stunt for Tesla, a company facing fresh delays 
 in production goals. And yes, the placement of a Tesla on top of a brand-new 230-foot-tall rocket with 27 engines, three times more than
 Musk’s flight-proven and reliable fleet of Falcon 9s, certainly helps with publicity.
</br>

The idea was to send the rocket - and the Roadster - around an elliptical orbit as far out as Mars, but only a few hours ago Musk 
tweeted that the “third burn” was successful, and the rocket exceeded the intended Mars orbit. It’s now headed towards an asteroid belt.

Still, if any roaming, sentient alien life forms happen to stumble upon the plucky ‘Starman’ and the Tesla Roadster - you know,
 as they casually go for a cruise THROUGH SPACE - they’ll find a calling card.

Musk had a special message printed on a circuit board inside the car - the same car that’s IN SPACE - which reads: “Made on Earth by humans”.
</br>
“We estimate it’ll be in that orbit for several hundred million years, maybe in excess of a billion years,” Musk noted before the rocket’s launch.




