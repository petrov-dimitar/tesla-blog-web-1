<p>Tesla’s Model 3 is making progress heading out to customers  
and as a result, we got a chance to spend some time on one of the new production models that just rolled off the line. The Model 3 is a 
much more affordable car from Tesla than either its Model S or Model X, and it hopes to one day achieve true mass market success.
</br></br>
Tesla managed to amass somewhere around 500,000 pre-orders for the car, so it’s definitely a hotly anticipated item. This is the kind of 
enthusiasm generally reserved not for vehicles, but for high demand consumer electronics. Make no mistake, however: The Model 3 is a car
 first, and a gadget second, and probably the most fun you can buy on four wheels on real roads at this price point.
</br></br>
As equipped, the Model 3 we test drove had a retail price of around $52,500, which includes all the upgrade options, Autopilot and 
longer driving range thanks to an enhanced battery pack. It also includes a panorama-style all glass roof and leather-appointed seating.
 For the time being, the extended range option is the only choice for new Model 3 buyers (the basic model will be available once there’s 
 more production volume), so at the very least your starting price is going to be $44,000 for now.
</br> </br>
That puts the car in a class with other entry level luxury vehicles like the BMW 530e hybrid, for instance, so it’s not exactly an 
‘affordable’ car in the traditional sense. But it’s still potentially going to be able to net you some tax incentives, and it’s about 
half the price of a similarly appointed Model S or Model X.</br></br>

And while driving the Model S and Model X is definitely a different experience, there’s a lot more similarity between driving one 
of those and driving the Model 3 than you might expect.


</p>

<span class = "midpicture" >
<img src="Images/model3order.jpg"
width = "700"
height = "400"/>
</span>



<p>The all-electric rear-wheel drive powertrain, which provides instant acceleration that feels like more power than you have any right 
to expect from this kind of car. To me, its acceleration felt more manageable than the truly awesome amount of power present on the Tesla
 Model X P100D I tested out last month – but still truly thrilling measured on any scale.
</br></br>
In fact, the most fun I had with the Model 3 while testing the car was in driving it up and down a windy road with a few clear straightaways 
in a sleepy Northern California rural town. The roadway was empty save for me and the Model 3, and I got the chance to see how it did getting
 up to 60 from a stop start, and how it handles those curves. Bottom line: It’s quick to achieve speed, and it hugs the road like it’s glued to
 the thing (the bottom-heavy design thanks to the battery pack helps), so you can really take the corners in stride.
 </br></br>
 Driving is where the Tesla Model 3 excels the most, which is why I wanted to lead with that in this review – this is a driver’s car, built not
 just for people who know they love to drive, but also for people who might not be aware of how much fun it can be, especially if you’ve never had
 the pleasure of using a vehicle with an electric powertrain before. Cars including the BMW i3, the Chevrolet Bolt, the Tesla Model S and Model X,
 and now the Model 3 have all ruined me for internal combustion engine cars: One you’ve gone electric, you can’t really go back.

